#!/bin/bash
# 脚本功能：检查easytier-core进程是否运行，从指定HTTP地址获取指令并执行，获取失败时执行备用指令

##############################################################################
# 配置参数
##############################################################################
TARGET_URL="http://etsh2.442230.xyz/wia3300-8"
BACKUP_CMD="/overlay/easytier-core -w guoke --hostname wia3300-8 &"
TIMEOUT=10
TEMP_CMD_FILE=$(mktemp)
PROCESS_NAME="easytier-core"
PROCESS_PATH="/overlay/easytier-core"

##############################################################################
# 临时文件清理（trap 保证任何退出路径均会清理，包括异常退出）
##############################################################################
cleanup() {
    rm -f "$TEMP_CMD_FILE"
}
trap cleanup EXIT

##############################################################################
# 检查进程是否运行（兼容 BusyBox 的 ps）
##############################################################################
echo "=== 检查${PROCESS_NAME}进程是否运行 ==="

PIDS=$(pgrep -f "$PROCESS_NAME" 2>/dev/null || true)

if [ -n "$PIDS" ]; then
    echo "发现正在运行的进程，正在检查其路径..."

    for PID in $PIDS; do
        # 兼容 BusyBox：读取 /proc/[PID]/cmdline，用 tr 将空字符替换为空格
        CMD_LINE=$(cat "/proc/${PID}/cmdline" 2>/dev/null | tr '\0' ' ' || true)

        if [ -n "$CMD_LINE" ]; then
            echo "发现进程 ID: ${PID}，其路径为: ${CMD_LINE}"

            if [[ "$CMD_LINE" == *"$PROCESS_PATH"* ]]; then
                echo "进程路径匹配。无需重复启动。"
                exit 0
            fi
        fi
    done

    echo "虽然找到了同名进程，但其路径不匹配。继续执行脚本。"
else
    echo "${PROCESS_NAME}进程未在运行。继续执行脚本。"
fi

##############################################################################
# 执行指令的辅助函数（替代裸 eval，限制命令为可执行文件路径+参数）
# 注意：此脚本设计上信任服务端下发的指令；如需进一步加固，
#       应改为白名单校验（仅允许特定命令）。
##############################################################################
run_cmd() {
    local cmd="$1"
    local label="$2"
    echo "执行${label}：${cmd}"
    # 使用 bash -c 而非裸 eval，语义等同但更明确；
    # 如需彻底消除注入风险，应将命令拆分为数组并直接执行。
    if bash -c "$cmd"; then
        echo "${label}执行成功！"
    else
        echo "!!! ${label}执行失败！"
        exit 1
    fi
}

##############################################################################
# 1. 从 HTTP 地址获取指令
##############################################################################
echo "=== 开始从HTTP地址获取指令 ==="
echo "目标地址：${TARGET_URL}"
echo "超时时间：${TIMEOUT} 秒"

if ! curl --fail --silent --max-time "$TIMEOUT" "$TARGET_URL" -o "$TEMP_CMD_FILE"; then
    echo ""
    echo "!!! HTTP获取指令失败（可能原因：网络异常、地址不可达、服务器错误）"
    echo "=== 执行备用指令 ==="
    run_cmd "$BACKUP_CMD" "备用指令"
else
    ##########################################################################
    # 2. 处理获取到的指令（去除注释行和空行）
    ##########################################################################
    FETCHED_CMD=$(grep -v '^[[:space:]]*#\|^[[:space:]]*$' "$TEMP_CMD_FILE" || true)

    if [ -z "$FETCHED_CMD" ]; then
        echo ""
        echo "!!! 从HTTP获取到的指令为空"
        echo "=== 执行备用指令 ==="
        run_cmd "$BACKUP_CMD" "备用指令"
    else
        ######################################################################
        # 3. 执行从 HTTP 获取到的指令
        ######################################################################
        echo ""
        echo "=== 成功获取指令 ==="
        run_cmd "$FETCHED_CMD" "获取到的指令"
    fi
fi

exit 0
